

/* Ano Bissexto
Leia um ano e informe se ele é bissexto ou não.*/

#include <stdio.h>
int main() {
   int dias;
   
   printf("digite quantos dias tem o ano: ");
   scanf("%i", &dias);
   
   if(dias = 365) {
    printf("ano não bissexto");
   } else {
       printf("ano bissexto");
   }
    
}


	





